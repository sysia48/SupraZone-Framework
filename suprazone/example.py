"""Example module for SupraZone Framework."""


def hello():
    """Return a friendly greeting message.
    
    Returns:
        str: A greeting message.
    """
    return "Hello from SupraZone Framework!"


def add(a, b):
    """Add two numbers together.
    
    Args:
        a: First number.
        b: Second number.
        
    Returns:
        The sum of a and b.
    """
    return a + b
